package Part_1;

public class P1_E9_Switch {
    public static void main(String[] args) {
        int i;

        for (i=0; i<5; i++){
            switch (i){
                case 0:
                    System.out.println(" i меньге 1 ");
                case 1:
                    System.out.println(" i меньге 2 ");
                case 2:
                    System.out.println(" i меньге 3 ");
                case 3:
                    System.out.println(" i меньге 4 ");
                case 4:
                    System.out.println(" i меньге 5 ");


            }
            System.out.println();
        }
    }
}
